// Seleciona o botão de login
const btnLogin = document.querySelector('.btn-login');

// Adiciona um ouvinte de evento para quando o cursor entra no botão
btnLogin.addEventListener('mouseenter', function() {
    btnLogin.classList.add('btn-login-hover');
});

// Adiciona um ouvinte de evento para quando o cursor sai do botão
btnLogin.addEventListener('mouseleave', function() {
    btnLogin.classList.remove('btn-login-hover');
});
